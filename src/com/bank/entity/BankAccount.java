package com.bank.entity;

public class BankAccount {
	private int ba_id;
	private String ba_name;
	private String ba_password;
	private String ba_sex;
	private String ba_tel;
	private String ba_email;
	private float ba_balance;
	private int ba_groupId;

	public int getBa_id() {
		return ba_id;
	}

	public void setBa_id(int ba_id) {
		this.ba_id = ba_id;
	}

	public String getBa_name() {
		return ba_name;
	}

	public void setBa_name(String ba_name) {
		this.ba_name = ba_name;
	}

	public String getBa_password() {
		return ba_password;
	}

	public void setBa_password(String ba_password) {
		this.ba_password = ba_password;
	}

	public String getBa_sex() {
		return ba_sex;
	}

	public void setBa_sex(String ba_sex) {
		this.ba_sex = ba_sex;
	}

	public String getBa_tel() {
		return ba_tel;
	}

	public void setBa_tel(String ba_tel) {
		this.ba_tel = ba_tel;
	}

	public String getBa_email() {
		return ba_email;
	}

	public void setBa_email(String ba_email) {
		this.ba_email = ba_email;
	}

	public float getBa_balance() {
		return ba_balance;
	}

	public void setBa_balance(float ba_balance) {
		this.ba_balance = ba_balance;
	}

	public int getBa_groupId() {
		return ba_groupId;
	}

	public void setBa_groupId(int ba_groupId) {
		this.ba_groupId = ba_groupId;
	}
	

}
